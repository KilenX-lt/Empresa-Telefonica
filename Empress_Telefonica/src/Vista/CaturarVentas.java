    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isBlank() | jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No puede haber vac√≠o en el ID de venta");
        } else {

            String id = jTextField1.getText().trim();
            double basein = (double) jSpinner1.getValue();
            double mmventas = (double) jSpinner1.getValue();

            Venta v1 = new Venta(id, basein, mmventas);
            Filehandler f2 = new Filehandler();

            try {
                f2.GuardarVenta(v1);
                JOptionPane.showMessageDialog(rootPane, "Venta guardado");
                
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            }
        }
        
        jTextField1.setText("");
        jSpinner1.setValue(0);
        jSpinner2.setValue(0);

    }//GEN-LAST:event_jButton1ActionPerformed

